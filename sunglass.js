function display(data) {
    console.log("data")
    data.map(function (el) {
        var card = document.createElement("div");
        card.setAttribute("id", "card")

        var image = document.createElement("img");
        image.src = el.image;

        var h3 = document.createElement("h5");
        h3.textContent = el.brand;

        var h1 = document.createElement("h4");
        h1.textContent = el.name;

        var p1 = document.createElement("p")
        p1.textContent = `$${el.original_price}`;
        p1.setAttribute("class", "para")

        var p2 = document.createElement("p")
        p2.textContent = `$${el.offer_price}`;

        var div2 = document.createElement("div")
        div2.setAttribute("class","box2")


        var disc = document.createElement("p");
        disc.textContent = `Save ${Math.round((el.offer_price - el.original_price) / el.offer_price * 100)}%`
        disc.setAttribute("class","disc")
        
        var div3 = document.createElement("div");
        div3.setAttribute("id", "price")

        div3.append(p1, p2)
        div2.append(h3, h1, div3,disc)
        card.append(image, div2);

        card.addEventListener("click", zoomin)
        function zoomin() {
            localStorage.setItem("zoomin", JSON.stringify(el))
            window.location.href = "productdetails.html"
        }

        document.querySelector("#right").append(card)

    })
}
